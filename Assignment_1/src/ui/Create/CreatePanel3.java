    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        person.getEmail().clear();
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        person.setTelePhone(txtPhoneNumber.getText());
        person.setFaxPhone(txtFaxNumber.getText());
        
        if(tableEmail.getRowCount() == 0){
            JOptionPane.showMessageDialog(this, "Please enter at least one email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            CreatePanel4 cp = new CreatePanel4(history, userProcessContainer,person);
            userProcessContainer.add("CreatePanel4",cp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if(txtEmail.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter the email", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        String emailType = (String) selectEmailType.getSelectedItem();
                
        person.addEmail(txtEmail.getText(), emailType);
        populateTable();
        txtEmail.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        DefaultTableModel model = (DefaultTableModel) tableEmail.getModel();
        int selectedEmailIndex = tableEmail.getSelectedRow();

        if(selectedEmailIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select an email to delete");
        } else {
            Email e = (Email) tableEmail.getValueAt(selectedEmailIndex, 1);
            person.deleteEmail(e);
            populateTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

