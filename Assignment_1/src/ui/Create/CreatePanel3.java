    private void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) tableEmail.getModel();
        model.setRowCount(0);
        
        for(Email e : person.getEmail()){
            Object[] row = new Object[2];
            row[0] = e.getType();
            row[1] = e;
            
            model.addRow(row);
        }
    }

